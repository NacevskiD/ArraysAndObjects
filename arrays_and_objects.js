/* b. Use this JavaScript object of exchange rates relative to Euros.
The properties are currency symbols, the values are the exchange rates.
Data source: http://fixer.io/
* */


let rates = {
    "AUD": 1.5417,
    "BGN": 1.9558,
    "BRL": 3.8959,
    "CAD": 1.5194
}

rates.CHF = 1.17873


let aud = 100 * rates.AUD
console.log("100 Euros to AUD = " +aud.toFixed(2))

let baseRate = 0
let currency = ''

for (const rate in rates){
    if (rates[rate] > baseRate){
        baseRate = rates[rate]
        currency = rate
    }
}

console.log(`Highest exchange rate is ${currency} with ${baseRate}`)

